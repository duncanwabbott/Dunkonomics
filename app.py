def check_and_fetch_data():
    files_to_check = [
        STANDINGS_FILE, 
        TEAM_STATS_FILE, 
        PLAYERS_FILE,
        os.path.join(DATA_DIR, "advanced_players.csv"),
        os.path.join(DATA_DIR, "cumfat.csv"),
        PLAYER_FOULS_FILE,
        TEAM_FOULS_FILE
    ]
    
    needs_fetch = False
    for filepath in files_to_check:
        if not os.path.exists(filepath):
            needs_fetch = True
            break
        
        # Check if older than 12 hours
        if os.path.exists(filepath):
            mtime = os.path.getmtime(filepath)
            file_age_hours = (time.time() - mtime) / 3600
            if file_age_hours > 12:
                needs_fetch = True
                break
            
    if needs_fetch:
        trigger_fetch_cycle()

