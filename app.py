# --- MATH HELPER ---
def american_to_implied(odds):
    if odds > 0: return 100 / (odds + 100)
    else: return abs(odds) / (abs(odds) + 100)

def implied_to_american(prob):
    if prob <= 0 or prob >= 1: return 0
    if prob > 0.5: return int(round(prob / (1 - prob) * -100))
    else: return int(round((1 - prob) / prob * 100))

# --- ARBITRAGE MATH ---
def calculate_arbitrage(odds1, odds2):
    """Calculates if an arbitrage opportunity exists between two odds."""
    prob1 = american_to_implied(odds1)
    prob2 = american_to_implied(odds2)
    total_prob = prob1 + prob2
    
    if total_prob < 1.0:
        profit_margin = (1.0 - total_prob) * 100
        # Optimal bet sizing assuming $100 total bankroll
        bet1 = (prob1 / total_prob) * 100
        bet2 = (prob2 / total_prob) * 100
        return True, profit_margin, bet1, bet2
    return False, 0, 0, 0

